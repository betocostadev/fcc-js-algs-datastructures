/* In JavaScript you can assign a value to a variable using the assingment operator (=) */
var one = 1;
var two = 2;
var result = one + two;
console.log(result);

/* We can create the variables and not assing them nothing at first,
and then assing a value later, or, we can assign a value at the start when we declare
the variables */

var a; // Declaration: valid
a = 10; // Assignment: valid
var b = 5, c = 15; // Declaring and assigning both at the same time: valid;

console.log('The variable a is: ' + a);
console.log(`The variable b is ${b} and
the variable c is ${c}`);