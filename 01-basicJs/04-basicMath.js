/* JavaScript Basic Math
Number is a data type in JavaScript which represents numeric data. */

var sum = 10 + 5;
// Output sum = 15;
console.log(`Sum = ${sum}`);
var difference = 45 - 10;
// Output difference = 35;
console.log(`Difference = ${difference}`);
var product = 8 * 10;
// Output product = 80;
console.log(`Product = ${product}`);
var quotient = 10 / 2;
// Output quotient = 5;
console.log(`Quotient = ${quotient}`);

// Increment a number:
var increment = 10;
increment++ // same as increment += 1 or increment = increment + 1;
// Output increment = 11;
console.log(`Increment = ${increment}`);

// Decrement a number:
increment-- // same as increment -= 1 or increment = increment - 1;
console.log(`Decrement = ${increment}`);

// Floating points, or floats
var myFloat = 2.5 * 2;
// Output myFloat = 5;
console.log(`Float = ${myFloat}`);

// Remainder (While it is similar to modulus, it doesn't work properly with negative values);
var remainder = 13 % 3;
console.log(`Remainder = ${remainder}`);

// Augmented Addition
var addMore = 15;
addMore += 20;
// Output = addMore = 35
console.log(`Augmented Addition = ${addMore}`);
// We also have augmented subtraction (-=), multiplication (*=) and division (/=)

